package com.entity;

public class Music {
    private String music_id;
    private String music_name;
    private String music_url;
    private String music_describe;
    private String music_singer;
    private String music_date;
    private String play_url;
	public String getMusic_id() {
		return music_id;
	}
	public void setMusic_id(String music_id) {
		this.music_id = music_id;
	}
	public String getPlay_url() {
		return play_url;
	}
	public void setPlay_url(String play_url) {
		this.play_url = play_url;
	}
	public String getMusic_name() {
		return music_name;
	}
	public void setMusic_name(String music_name) {
		this.music_name = music_name;
	}
	public String getMusic_url() {
		return music_url;
	}
	public void setMusic_url(String music_url) {
		this.music_url = music_url;
	}
	public String getMusic_describe() {
		return music_describe;
	}
	public void setMusic_describe(String music_describe) {
		this.music_describe = music_describe;
	}
	public String getMusic_singer() {
		return music_singer;
	}
	public void setMusic_singer(String music_singer) {
		this.music_singer = music_singer;
	}
	public String getMusic_date() {
		return music_date;
	}
	public void setMusic_date(String music_date) {
		this.music_date = music_date;
	}
	

}
