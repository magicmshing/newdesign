package com.entity;

public class Speech {
	private String speech_id;
	private String speech_user;
	private String speech_content;
	private String speech_date;
	public String getSpeech_id() {
		return speech_id;
	}
	public void setSpeech_id(String speech_id) {
		this.speech_id = speech_id;
	}
	public String getSpeech_user() {
		return speech_user;
	}
	public void setSpeech_user(String speech_user) {
		this.speech_user = speech_user;
	}
	public String getSpeech_content() {
		return speech_content;
	}
	public void setSpeech_content(String speech_content) {
		this.speech_content = speech_content;
	}
	public String getSpeech_date() {
		return speech_date;
	}
	public void setSpeech_date(String speech_date) {
		this.speech_date = speech_date;
	}
	

}
